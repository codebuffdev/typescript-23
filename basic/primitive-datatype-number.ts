// It can be signed integer.
let singed: number = -10; //+ or - sign

//It can be unsigned integer.
let unsigned: number = 10; // without any sign

//It can be floating point.
let Float: number = 34.34; // 2-digit integer & fraction value both

//It can be double.
let double: number = 434.45; // more than 2-digit, fraction upto 29 decimal places

// It can be exponent.
let exponent1: number = 2e3; //2 * *10 pow 3*
let exponent2: number = 2e2; //2 ** 10 pow 2

//It can be Hexadecimal.
let hexaDecimal: number = 0xf00d;

//It can be Binary.
let binary: number = 0b1010;

//It can be Octal.
let octal: number = 0o744;

//It can be Decimal.
let decimal: number = 45.768686; //29 decimal point
